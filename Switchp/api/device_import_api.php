// Normalize MAC address
function normalizeMac($mac) {
    $mac = strtoupper(preg_replace('/[^0-9A-Fa-f]/', '', $mac));
    if (strlen($mac) === 12) {
        return implode(':', str_split($mac, 2));
    }
    return null;
}

// Validate IP address
function validateIP($ip) {
    return filter_var($ip, FILTER_VALIDATE_IP) !== false;
}

/**
 * Return true when $mac is a locally-administered (randomised / virtual) address.
 *
 * The IEEE 802 standard reserves bit 1 of the first octet to indicate whether a
 * MAC was globally assigned (OUI-based) or locally administered (e.g. randomly
 * generated by a WiFi client or a virtual-machine hypervisor).  Such addresses
 * must never be treated as unregistered physical devices.
 *
 * @param  string $mac  Colon-separated or plain-hex MAC in any case (e.g. "7A:B3:…")
 * @return bool
 */
function isLocallyAdministeredMac(string $mac): bool {
    $firstOctet = hexdec(substr(str_replace(':', '', $mac), 0, 2));
    return (bool)($firstOctet & 0x02);
}

/**
 * Return true when the error message indicates a SQL Server or MySQL deadlock.
 * SQL Server deadlocks have SQLSTATE 40001 (error 1205) in the error string.
 * MySQL deadlocks have error code 1213 in the error string.
 */
function isDeadlockError(?string $errorMsg): bool {
    if (!$errorMsg) return false;
    return strpos($errorMsg, '40001') !== false
        || strpos($errorMsg, '1205')  !== false
        || strpos($errorMsg, '1213')  !== false
        || stripos($errorMsg, 'deadlock') !== false;
}

/**
 * Execute a prepared statement with automatic deadlock retry.
 * On SQL Server (SQLSTATE 40001) or MySQL (1213) deadlocks, the statement
 * is re-executed up to $maxRetries times with exponential back-off.
 *
 * @param  object $stmt        Prepared MysqliPdoStmt (already bound)
 * @param  int    $maxRetries  Maximum additional attempts after the first failure
 * @return bool                True on success
 */
function executeWithDeadlockRetry(object $stmt, int $maxRetries = 5): bool {
    for ($attempt = 0; $attempt <= $maxRetries; $attempt++) {
        $ok = $stmt->execute();
        if ($ok) return true;
        if ($attempt < $maxRetries && isDeadlockError($stmt->error ?? '')) {
            // SQL Server deadlock detection runs every ~5 s, so the back-off
            // must be long enough to let the lock cycle clear.
            // Exponential back-off starting at 1 s: 1 s, 2 s, 4 s, 8 s, 16 s.
            usleep(1000000 * (1 << $attempt));
            continue;
        }
        break;
    }
    // Log only after all retries are exhausted.
    if ($stmt->error) {
        error_log("executeWithDeadlockRetry failed after {$maxRetries} retries: " . $stmt->error);
    }
    return false;
}

/**
 * Read SMTP config from snmp_worker/config/config.yml and return parsed email settings.
 * Returns null if email is disabled or config unreadable.
 */
function getSmtpConfig() {
    $configPath = __DIR__ . '/../snmp_worker/config/config.yml';
    if (!file_exists($configPath)) return null;
    $content = @file_get_contents($configPath);
    if (!$content) return null;

    // Extract the email: block using regex (same approach as snmp_admin.php)
    if (!preg_match(
        '/email:\s+enabled:\s*(true|false)\s+smtp_host:\s*"([^"]*)"\s+smtp_port:\s*([^\s]+)\s+smtp_user:\s*"([^"]*)"\s+smtp_password:\s*"([^"]*)"\s+from_address:\s*"([^"]*)"/s',
        $content, $m
    )) return null;

    if ($m[1] !== 'true') return null; // email disabled

    // Parse to_addresses list
    $toAddresses = [];
    if (preg_match('/to_addresses:\s*((?:\s*-\s*"[^"]*"\s*)+)/', $content, $tm)) {
        preg_match_all('/-\s*"([^"]*)"/', $tm[1], $addrMatches);
        $toAddresses = array_filter($addrMatches[1]);
    }

    if (empty($toAddresses)) return null;

    return [
        'smtp_host'     => $m[2],
        'smtp_port'     => (int)$m[3],
        'smtp_user'     => $m[4],
        'smtp_password' => $m[5],
        'from_address'  => $m[6],
        'to_addresses'  => array_values($toAddresses),
    ];
}

/**
 * Get the list of recipient email addresses for a given mail type.
 * Queries alarm_user_email_config for per-user settings; falls back to global to_addresses.
 *
 * @param string $mailType  e.g. 'excel_export', 'mac_history_cleanup'
 * @return array  Email address strings, or empty array if none configured
 */
function getMailTypeRecipients(string $mailType): array {
    global $conn;
    try {
        // Table may not exist yet — create it if missing
        $conn->query(
            "IF OBJECT_ID('dbo.alarm_user_email_config','U') IS NULL BEGIN " .
            "CREATE TABLE alarm_user_email_config (" .
            "alarm_type VARCHAR(100) NOT NULL, user_id INT NOT NULL, email_enabled BIT NOT NULL DEFAULT 1, " .
            "CONSTRAINT PK_alarm_user_email_config PRIMARY KEY (alarm_type, user_id)" .
            ") END"
        );
        // Check whether any per-type rows exist for this mail type
        $chk = $conn->prepare(
            "SELECT COUNT(*) AS cnt FROM alarm_user_email_config WHERE alarm_type = ?"
        );
        $chk->bind_param('s', $mailType);
        $chk->execute();
        $row = $chk->get_result()->fetch_assoc();
        $chk->close();
        if ((int)($row['cnt'] ?? 0) === 0) {
            // No per-type config — fall back to global SMTP to_addresses
            $cfg = getSmtpConfig();
            return $cfg ? $cfg['to_addresses'] : [];
        }
        // Return emails of users opted-in for this mail type
        $stmt = $conn->prepare(
            "SELECT u.email FROM alarm_user_email_config auc " .
            "JOIN users u ON u.id = auc.user_id " .
            "WHERE auc.alarm_type = ? AND auc.email_enabled = 1 " .
            "AND u.is_active = 1 AND u.email IS NOT NULL AND LEN(LTRIM(u.email)) > 0"
        );
        $stmt->bind_param('s', $mailType);
        $stmt->execute();
        $result = $stmt->get_result();
        $emails = [];
        while ($r = $result->fetch_assoc()) {
            $emails[] = $r['email'];
        }
        $stmt->close();
        return $emails;
    } catch (\Throwable $e) {
        // On any error fall back to global list
        $cfg = getSmtpConfig();
        return $cfg ? $cfg['to_addresses'] : [];
    }
}

/**
 * Send an alarm email via SMTP (sync, blocking).
 * Uses fsockopen for port 465 (SSL) or stream_socket_client + STARTTLS for port 587.
 *
 * @param string $subject
 * @param string $htmlBody
 * @param array|null $recipients  Optional override recipient list; defaults to global to_addresses
 * @return bool
 */
function sendAlarmEmail(string $subject, string $htmlBody, array $recipients = null): bool {
    $cfg = getSmtpConfig();
    if (!$cfg) return false;
    if ($recipients !== null) {
        $cfg['to_addresses'] = $recipients;
    }
    if (empty($cfg['to_addresses'])) return false;

    $boundary = md5(uniqid());
    $plainBody = strip_tags(str_replace(['<br>', '<br/>', '<br />'], "\n", $htmlBody));

    $message = "MIME-Version: 1.0\r\n"
        . "Content-Type: multipart/alternative; boundary=\"{$boundary}\"\r\n"
        . "\r\n"
        . "--{$boundary}\r\n"
        . "Content-Type: text/plain; charset=UTF-8\r\n\r\n"
        . $plainBody . "\r\n"
        . "--{$boundary}\r\n"
        . "Content-Type: text/html; charset=UTF-8\r\n\r\n"
        . $htmlBody . "\r\n"
        . "--{$boundary}--\r\n";

    $port = $cfg['smtp_port'];
    $host = $cfg['smtp_host'];
    $user = $cfg['smtp_user'];
    $pass = $cfg['smtp_password'];
    $from = $cfg['from_address'] ?: $user;
    $tos  = $cfg['to_addresses'];

    try {
        $useImplicitTLS = ($port === 465);
        $proto  = $useImplicitTLS ? 'ssl' : 'tcp';
        $ctx    = stream_context_create(['ssl' => ['verify_peer' => false, 'verify_peer_name' => false]]);
        $sock   = @stream_socket_client("{$proto}://{$host}:{$port}", $errno, $errstr, 15,
                                        STREAM_CLIENT_CONNECT, $ctx);
        if (!$sock) return false;
        stream_set_timeout($sock, 15);

        // Read ALL lines of a multi-line SMTP response (e.g. EHLO returns 250-... lines)
        $read = function() use ($sock) {
            $buf = '';
            while (($line = fgets($sock, 1024)) !== false) {
                $buf .= $line;
                if (isset($line[3]) && $line[3] === ' ') break; // last line has space as 4th char
            }
            return trim($buf);
        };

        $greeting = $read();
        if (substr($greeting, 0, 3) !== '220') { fclose($sock); return false; }

        fwrite($sock, "EHLO " . gethostname() . "\r\n");
        $read(); // consume full multi-line EHLO response

        // STARTTLS upgrade for port 587
        if (!$useImplicitTLS) {
            fwrite($sock, "STARTTLS\r\n");
            $r = $read();
            if (substr($r, 0, 3) !== '220') { fclose($sock); return false; }
            if (!stream_socket_enable_crypto($sock, true, STREAM_CRYPTO_METHOD_TLS_CLIENT)) {
                fclose($sock); return false;
            }
            fwrite($sock, "EHLO " . gethostname() . "\r\n");
            $read(); // consume post-STARTTLS EHLO
        }

        // AUTH LOGIN
        fwrite($sock, "AUTH LOGIN\r\n");
        $r = $read();
        if (substr($r, 0, 3) !== '334') { fclose($sock); return false; }
        fwrite($sock, base64_encode($user) . "\r\n");
        $read();
        fwrite($sock, base64_encode($pass) . "\r\n");
        $r = $read();
        if (substr($r, 0, 3) !== '235') { fclose($sock); return false; }

        // Envelope
        fwrite($sock, "MAIL FROM:<{$from}>\r\n"); $read();
        foreach ($tos as $to) {
            fwrite($sock, "RCPT TO:<{$to}>\r\n"); $read();
        }

        // Body
        fwrite($sock, "DATA\r\n"); $read();
        $headers = "From: {$from}\r\nTo: " . implode(', ', $tos) . "\r\nSubject: {$subject}\r\nDate: " . date('r') . "\r\n";
        fwrite($sock, $headers . $message . "\r\n.\r\n");
        $r = $read();
        fwrite($sock, "QUIT\r\n");
        fclose($sock);
        return substr($r, 0, 3) === '250';
    } catch (\Throwable $e) {
        error_log("sendAlarmEmail error: " . $e->getMessage());
        return false;
    }
}

/**
 * Log an email send to the email_log table (best-effort, ignores errors).
 */
function logEmailSent(string $subject, array $recipients, string $mailType, bool $success, string $alarmType = null): void {
    global $conn;
    try {
        $tableCheck = $conn->query("SELECT COUNT(*) as cnt FROM information_schema.tables WHERE table_name='email_log'");
        if (!$tableCheck || !(($r = $tableCheck->fetch_assoc()) && (int)$r['cnt'])) return;
        $recipientsStr = implode(', ', $recipients);
        $status = $success ? 'sent' : 'failed';
        $stmt = $conn->prepare(
            "INSERT INTO email_log (sent_at, subject, recipients, alarm_type, mail_type, status) VALUES (NOW(), ?, ?, ?, ?, ?)"
        );
        $stmt->bind_param('sssss', $subject, $recipientsStr, $alarmType, $mailType, $status);
        $stmt->execute();
        $stmt->close();
    } catch (\Throwable $e) {
        // Silently ignore
    }
}

// ── Lightweight native xlsx / csv reader (no vendor dependency) ──────────────

/**
 * Converts a column letter string (A, B, AA, ...) to a zero-based index.
 */
function _colLetterToIndex(string $col): int {
    $idx = 0;
    $len = strlen($col);
    for ($i = 0; $i < $len; $i++) {
        $idx = $idx * 26 + (ord($col[$i]) - 64);
    }
    return $idx - 1;
}

/**
 * Parse an xlsx file and return its first sheet as a 2-D array.
 * Requires ZipArchive and SimpleXML (both bundled with PHP).
 */
function parseXlsx(string $filePath): array {
    $zip = new ZipArchive();
    if ($zip->open($filePath) !== true) {
        throw new \RuntimeException('xlsx dosyası açılamadı.');
    }

    // Shared strings table
    $sharedStrings = [];
    $ssRaw = $zip->getFromName('xl/sharedStrings.xml');
    if ($ssRaw !== false) {
        $ss = simplexml_load_string($ssRaw);
        foreach ($ss->si as $si) {
            if (isset($si->t)) {
                $sharedStrings[] = (string) $si->t;
            } else {
                $text = '';
                foreach ($si->r as $r) {
                    $text .= (string) $r->t;
                }
                $sharedStrings[] = $text;
            }
        }
    }

    // First worksheet
    $sheetRaw = $zip->getFromName('xl/worksheets/sheet1.xml');
    $zip->close();

    if ($sheetRaw === false) {
        throw new \RuntimeException('Çalışma sayfası okunamadı.');
    }

    $sheet = simplexml_load_string($sheetRaw);
    $rows  = [];

    foreach ($sheet->sheetData->row as $row) {
        $rowData  = [];
        $lastCol  = -1;

        foreach ($row->c as $cell) {
            if (!preg_match('/([A-Z]+)/', (string) $cell['r'], $m)) {
                continue;
            }
            $col = _colLetterToIndex($m[1]);

            // Fill any skipped (empty) columns
            for ($i = $lastCol + 1; $i < $col; $i++) {
                $rowData[] = null;
            }
            $lastCol = $col;

            $type  = (string) ($cell['t'] ?? '');
            $value = (string) ($cell->v ?? '');

            if ($type === 's') {
                $rowData[] = $sharedStrings[(int) $value] ?? '';
            } elseif ($type === 'inlineStr') {
                $rowData[] = (string) ($cell->is->t ?? '');
            } elseif ($type === 'b') {
                $rowData[] = $value === '1';
            } else {
                $rowData[] = $value === '' ? null : $value;
            }
        }

        $rows[] = $rowData;
    }

    return $rows;
}

/**
 * Read an xlsx or csv file and return rows as a 2-D array (like PhpSpreadsheet toArray()).
 */
function readSpreadsheetFile(string $filePath, string $ext): array {
    if ($ext === 'csv') {
        $rows = [];
        if (($handle = fopen($filePath, 'r')) !== false) {
            // Detect and skip UTF-8 BOM
            $bom = fread($handle, 3);
            if (strlen($bom) < 3 || $bom !== "\xEF\xBB\xBF") {
                rewind($handle);
            }
            while (($row = fgetcsv($handle)) !== false) {
                $rows[] = $row;
            }
            fclose($handle);
        }
        return $rows;
    }

    return parseXlsx($filePath);
}

// ── Lightweight native xlsx writer (no vendor dependency) ────────────────────

/**
 * Convert a zero-based column index to an Excel column letter (0→A, 25→Z, 26→AA …).
 */
function _colIndexToLetter(int $idx): string {
    $letter = '';
    $n = $idx + 1;
    while ($n > 0) {
        $rem    = ($n - 1) % 26;
        $letter = chr(65 + $rem) . $letter;
        $n      = (int)(($n - 1) / 26);
    }
    return $letter;
}

/**
 * Escape a string value for embedding in an XML attribute or element.
 */
function _xmlEsc(string $v): string {
    return htmlspecialchars($v, ENT_XML1 | ENT_QUOTES, 'UTF-8');
}

/**
 * Build a minimal, valid .xlsx binary string from a 2-D array.
 *
 * Row 0 is treated as the header row and rendered bold.
 * All values are written as inline strings (t="inlineStr") so no shared-strings
 * table is needed.  Requires ZipArchive (bundled with PHP).
 *
 * @param array $rows  2-D array [ [col0, col1, …], … ]
 * @return string      Raw binary content of the .xlsx file
 */
function buildXlsx(array $rows): string {
    // ── worksheet XML ─────────────────────────────────────────────────────────
    $sheetRows = '';
    foreach ($rows as $ri => $rowData) {
        $rowNum  = $ri + 1;
        $isHdr   = ($ri === 0);
        $cells   = '';
        foreach ($rowData as $ci => $val) {
            $colLetter = _colIndexToLetter($ci);
            $ref       = $colLetter . $rowNum;
            $safe      = _xmlEsc((string)($val ?? ''));
            // s="1" = bold style (defined in styles.xml); s="0" = normal
            $style     = $isHdr ? ' s="1"' : '';
            $cells    .= "<c r=\"{$ref}\" t=\"inlineStr\"{$style}><is><t>{$safe}</t></is></c>";
        }
        $sheetRows .= "<row r=\"{$rowNum}\">{$cells}</row>";
    }

    $sheetXml = '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        . '<worksheet xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main">'
        . '<sheetData>' . $sheetRows . '</sheetData>'
        . '</worksheet>';

    // ── styles.xml (2 xf: 0=normal, 1=bold header) ───────────────────────────
    $stylesXml = '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        . '<styleSheet xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main">'
        . '<fonts count="2">'
        .   '<font><sz val="11"/><name val="Calibri"/></font>'
        .   '<font><b/><sz val="11"/><name val="Calibri"/></font>'
        . '</fonts>'
        . '<fills count="2">'
        .   '<fill><patternFill patternType="none"/></fill>'
        .   '<fill><patternFill patternType="gray125"/></fill>'
        . '</fills>'
        . '<borders count="1">'
        .   '<border><left/><right/><top/><bottom/><diagonal/></border>'
        . '</borders>'
        . '<cellStyleXfs count="1"><xf numFmtId="0" fontId="0" fillId="0" borderId="0"/></cellStyleXfs>'
        . '<cellXfs count="2">'
        .   '<xf numFmtId="0" fontId="0" fillId="0" borderId="0" xfId="0"/>'
        .   '<xf numFmtId="0" fontId="1" fillId="0" borderId="0" xfId="0" applyFont="1"/>'
        . '</cellXfs>'
        . '</styleSheet>';

    // ── workbook & relationship XML ───────────────────────────────────────────
    $workbookXml = '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        . '<workbook xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main"'
        . ' xmlns:r="http://schemas.openxmlformats.org/officeDocument/2006/relationships">'
        . '<sheets><sheet name="Kayıtlar" sheetId="1" r:id="rId1"/></sheets>'
        . '</workbook>';

    $wbRelsXml = '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        . '<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">'
        . '<Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/worksheet" Target="worksheets/sheet1.xml"/>'
        . '<Relationship Id="rId2" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/styles" Target="styles.xml"/>'
        . '</Relationships>';

    $dotRelsXml = '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        . '<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">'
        . '<Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" Target="xl/workbook.xml"/>'
        . '</Relationships>';

    $contentTypesXml = '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        . '<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">'
        . '<Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>'
        . '<Default Extension="xml" ContentType="application/xml"/>'
        . '<Override PartName="/xl/workbook.xml" ContentType="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet.main+xml"/>'
        . '<Override PartName="/xl/worksheets/sheet1.xml" ContentType="application/vnd.openxmlformats-officedocument.spreadsheetml.worksheet+xml"/>'
        . '<Override PartName="/xl/styles.xml" ContentType="application/vnd.openxmlformats-officedocument.spreadsheetml.styles+xml"/>'
        . '</Types>';

    // ── assemble into a ZIP (xlsx = ZIP) ──────────────────────────────────────
    $tmp = tempnam(sys_get_temp_dir(), 'xlsx_');
    $zip = new ZipArchive();
    if ($zip->open($tmp, ZipArchive::OVERWRITE) !== true) {
        throw new \RuntimeException('Geçici xlsx dosyası oluşturulamadı.');
    }
    $zip->addFromString('[Content_Types].xml',          $contentTypesXml);
    $zip->addFromString('_rels/.rels',                  $dotRelsXml);
    $zip->addFromString('xl/workbook.xml',              $workbookXml);
    $zip->addFromString('xl/_rels/workbook.xml.rels',   $wbRelsXml);
    $zip->addFromString('xl/styles.xml',                $stylesXml);
    $zip->addFromString('xl/worksheets/sheet1.xml',     $sheetXml);
    $zip->close();

    $content = file_get_contents($tmp);
    unlink($tmp);
    return $content;
}

