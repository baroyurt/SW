            }

            // ── Phase 3: Send email notification if new alarms were created ──
            if ($scan_created > 0) {
                $subject = "CHAMADA Kayıtsız MAC Alarm Özeti";
                $macContent = "<p style='margin:0 0 14px;font-size:15px;font-weight:700;color:#f59e0b;'>Kayıtsız MAC Alarm Özeti</p>"
                    . "<p style='margin:4px 0;'><strong style='color:#8899bb;'>Tarama sonucu:</strong> {$scan_total} kayıtsız MAC tespit edildi.</p>"
                    . "<p style='margin:4px 0;'><strong style='color:#8899bb;'>Oluşturulan alarmlar:</strong> <span style='color:#e74c3c;font-weight:700;'>{$scan_created}</span></p>"
                    . "<p style='margin:4px 0;'><strong style='color:#8899bb;'>Atlanan (mevcut aktif alarm):</strong> {$scan_skipped}</p>"
