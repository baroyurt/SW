                LEFT JOIN mac_device_registry mdr
                       ON UPPER(REPLACE(mdr.mac_address, ':', '')) COLLATE utf8mb4_general_ci
                        = UPPER(REPLACE(mat.mac_address, ':', '')) COLLATE utf8mb4_general_ci
